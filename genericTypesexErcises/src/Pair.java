class Pair <T, K> {

    private T instancjaT;
    private K instancjaK;

    public T getInstancjaT() {
        return instancjaT;
    }
    public void setInstancjaT(T instancjaT) {
        this.instancjaT = instancjaT;
    }
    public K getInstancjaK() {
        return instancjaK;
    }
    public void setInstancjaK(K instancjaK) {
        this.instancjaK = instancjaK;
    }
}
