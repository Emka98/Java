class Point {

    int X = 0;
    int Y = 0;

    public Point (int X, int Y){
        this.X = X;
        this.Y = Y;
    }

    public Point (int X){
        this.X = X;
    }

    public Point (int Y){
        this.Y = Y;
    }

    public Point (){
    }

    public int getX() {
        return X;
    }

    public void setX(int x) {
        X = x;
    }

    public int getY() {
        return Y;
    }

    public void setY(int y) {
        Y = y;
    }

    






    
}
